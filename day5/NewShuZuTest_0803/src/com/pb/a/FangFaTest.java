package com.pb.a;

public class FangFaTest {

	/*
	 * 方法的基本结构:
	 * 
	 * public void 方法名( ){ 方法体-方法中的代码 } public:修饰符void： 返回类型
	 * （如果使用的是void，那么返回就是空，指方法不需要返回）方法名( )：遵循标识符的定义规则{方法体-方法中的代码（逻辑操作）}
	 * 
	 * 是整个方法的方法体
	 */

	// 是整个程序入口，里边放的应用程序的所有逻辑操作
	public static void main(String[] args) {

		eat();

	}

	// 自定义方法
	public static void eat() {

		System.out.println("agasg");

	}
	
	
	// 自定义方法
	public static void sleep() {

		System.out.println("agasg");

	}
	
	
	// 自定义方法
	public static void speek() {

		System.out.println("agasg");

	}

}
